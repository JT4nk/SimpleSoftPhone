    private void configure(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_configure
        try {
            link = new SipLink(
                    localSipAddressIpTextField.getText(),
                    Integer.parseInt(localSipAddressPortTextField.getText()),
                    Integer.parseInt(localRtpAddressPortTextField.getText()),
                    this);
            disableConfiguration();
            callButton.setEnabled(true);
        } catch (InvalidArgumentException e) {
            e.printStackTrace();
            enableConfiguration();
            callButton.setEnabled(false);
        }
    }//GEN-LAST:event_configure

    private void call() {//GEN-FIRST:event_call
        if (callNumberTextField.getText().equals("")) {
            calleeAddress = SipLink.uriFromAddress(
                    destSipAddressIpTextField.getText(),
                    Integer.parseInt(destSipAddressPortTextField.getText())
            );
            link.call(calleeAddress);
        } else {
            link.callByNumber(callNumberTextField.getText());
        }


        establishing = true;

        callPanel.setVisible(false);
        callingPanel.setVisible(true);
        callActionsPanel.add(callingPanel, java.awt.BorderLayout.PAGE_END);
        callingContentLabel.setText("Etablissement de la connexion");

    }//GEN-LAST:event_call

    private void register() {
        String proxyAddress = SipLink.uriFromAddress(
                proxySipAddressIpTextField.getText(),
                Integer.parseInt(proxySipAddressPortTextField.getText())
        );
        link.register(proxyAddress, numberTextField.getText());
        disableRegistration();
    }

    private void registerService() {
        String address = SipLink.uriFromAddress(
                divertSipAddressIpTextField.getText(),
                Integer.parseInt(divertSipAddressPortTextField.getText())
        );
        link.registerService(address, divertConditionComboBox.getSelectedIndex() == 0);
        disableServiceRegistration();
    }

    private void cancelService() {
        link.cancelService();
        enableServiceRegistration();
    }

    public Boolean isCalling() {
        return calling;
    }

    public void ringing() {
        callingContentLabel.setText("Sonnerie...");
        playRinging();
        ringing = true;
    }

    private void hangUp() {
        incomingCallPanel.setVisible(false);
        callingPanel.setVisible(false);
        callPanel.setVisible(true);
        callActionsPanel.add(callPanel, java.awt.BorderLayout.PAGE_END);
        stopSound();
        if (calling) {
            link.endCall();
        } else if (establishing || ringing) {
            link.cancelCall();
        } else {
            link.hangUp();
        }
        resetState();
    }

    public void incomingCall(String from) {
        playRingtone();
        callPanel.setVisible(false);
        incomingCallPanel.setVisible(true);
        callActionsPanel.add(incomingCallPanel, java.awt.BorderLayout.PAGE_END);
        incomingCallContentLabel.setText(from);
    }

    public void answeredCall() {
        stopSound();
        calling = true;
        callingContentLabel.setText("Appel en cours...");
    }

    public void registrationFailed() {
        enableRegistration();
    }

    public void takeCall() {
        stopSound();
        link.takeCall();
        calling = true;
        incomingCallPanel.setVisible(false);
        callingPanel.setVisible(true);
        callActionsPanel.add(callingPanel, java.awt.BorderLayout.PAGE_END);
        callingContentLabel.setText("Appel en cours...");
    }

    public void cancelCall() {
        stopSound();
        incomingCallPanel.setVisible(false);
        callingPanel.setVisible(false);
        callPanel.setVisible(true);
        callActionsPanel.add(callPanel, java.awt.BorderLayout.PAGE_END);
        resetState();
    }

    private void resetState() {
        establishing = false;
        ringing = false;
        calling = false;
    }

    private void playRinging() {
        playSound(RINGING_MEDIA);
    }

    private void playRingtone() {
        playSound(RINGTONE_MEDIA);
    }

    private void playSound(String locator) {
        try {
            File soundFile = new File(locator);
            AudioInputStream audioIn = AudioSystem.getAudioInputStream(soundFile);
            clip = AudioSystem.getClip();
            clip.open(audioIn);
            clip.loop(Clip.LOOP_CONTINUOUSLY);
            clip.start();
        } catch (UnsupportedAudioFileException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } catch (LineUnavailableException e) {
            e.printStackTrace();
        }
    }

    private void stopSound() {
        if (clip != null) {
            clip.stop();
        }
    }

